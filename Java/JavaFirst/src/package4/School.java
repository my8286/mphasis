package package4;

public class School {	
	private String name;
	private String board;
	private String city;
	private Student studend;
	public Student getStudend() {
		return studend;
	}
	public void setStudend(Student studend) {
		this.studend = studend;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getBoard() {
		return board;
	}
	public void setBoard(String board) {
		this.board = board;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	
}
